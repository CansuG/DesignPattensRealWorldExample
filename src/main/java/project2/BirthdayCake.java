package project2;

public class BirthdayCake extends Cake {
    public BirthdayCake() {

        this.name = "Birthday Cake";
        this.numLayers = 1;
        this.flavor = "Funfetti";
        this.icing = "Vanilla";
        decorations.add("Sprinkles");
        decorations.add("Candy Flowers");
        decorations.add("Icing Writing");
    }

}